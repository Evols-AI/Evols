const GRID = 60
const W = 1400
const H = 730
const LOGO_SIZE = 18
const LOGO_SCALE = LOGO_SIZE / 24

// Evols hub position (snapped to grid)
const EX = 720, EY = 60

// Brand logos at grid intersections
const NODES: { key: string; label: string; x: number; y: number }[] = [
  { key: 'Cursor',     label: 'Cursor',       x: 120,  y: 120 },
  { key: 'Claude',     label: 'Claude',       x: 180,  y: 300 },
  { key: 'Zoom',       label: 'Zoom',         x: 240,  y: 480 },
  { key: 'Copilot',    label: 'Copilot',      x: 420,  y: 600 },
  { key: 'Slack',      label: 'Slack',        x: 1320, y: 120 },
  { key: 'Salesforce', label: 'Salesforce',   x: 1260, y: 300 },
  { key: 'Zendesk',    label: 'Zendesk',      x: 1200, y: 480 },
  { key: 'G-Drive',    label: 'Google Drive', x: 1020, y: 600 },
]

// Grid-following paths from each logo to Evols hub.
// Routes go vertically to EY=120 first, then horizontally to EX=720
// (or straight horizontal if already on the same row).
function gridPath(n: { x: number; y: number }): string {
  if (n.y === EY) return `M ${n.x} ${n.y} L ${EX} ${EY}`
  return `M ${n.x} ${n.y} L ${n.x} ${EY} L ${EX} ${EY}`
}

export default function HeroBridgeBackground({ dark }: Props) {
  const gridColor  = dark ? 'rgba(160,130,255,0.18)'  : 'rgba(0,0,0,0.04)'
  const logoColor  = dark ? 'rgba(160,130,255,0.30)'  : 'rgba(0,0,0,0.10)'
  const labelColor = dark ? 'rgba(160,130,255,0.13)'  : 'rgba(0,0,0,0.05)'
  const dotColor   = dark ? 'rgba(160,130,255,0.65)'  : 'rgba(0,0,0,0.28)'

  return (
    <svg
      className="absolute inset-0 w-full h-full pointer-events-none"
      viewBox={`0 0 ${W} ${H}`}
      preserveAspectRatio="xMidYMid slice"
      aria-hidden="true"
    >
      <defs>
        <pattern id="hbg-grid" width={GRID} height={GRID} patternUnits="userSpaceOnUse">
          <path d={`M ${GRID} 0 L 0 0 0 ${GRID}`} fill="none" stroke={gridColor} strokeWidth="1" />
        </pattern>

        {/* Named paths for animateMotion — one per logo, grid-following route to Evols */}
        {NODES.map((n, i) => (
          <path key={i} id={`hbg-p${i}`} d={gridPath(n)} fill="none" />
        ))}
      </defs>

      {/* Grid */}
      <rect width={W} height={H} fill="url(#hbg-grid)" />

      {/* Dots travelling between each logo and Evols along grid lines */}
      {NODES.map((_, i) => {
        const dur = 12 + i * 1.4
        const fwd = i % 2 === 0
        return (
          <circle key={`d${i}`} r="1.8" fill={dotColor}>
            <animateMotion
              dur={`${dur.toFixed(1)}s`}
              repeatCount="indefinite"
              begin={`${(-(dur * i / NODES.length)).toFixed(1)}s`}
              calcMode="linear"
              keyPoints={fwd ? '0;1' : '1;0'}
              keyTimes="0;1"
            >
              <mpath href={`#hbg-p${i}`} />
            </animateMotion>
          </circle>
        )
      })}

      {/* Brand logos */}
      {NODES.map((n, i) => (
        <g key={`n${i}`}>
          <g transform={`translate(${n.x - LOGO_SIZE / 2}, ${n.y - LOGO_SIZE / 2}) scale(${LOGO_SCALE})`}>
            <path d={LOGOS[n.key]} fill={logoColor} />
          </g>
          <text x={n.x} y={n.y + LOGO_SIZE / 2 + 11}
            textAnchor="middle" fontSize="9"
            fontFamily="system-ui,-apple-system,sans-serif" fontWeight="500"
            fill={labelColor}>
            {n.label}
          </text>
        </g>
      ))}

      {/* Evols hub — favicon rendered via <image> to avoid path corruption */}
      <image
        href="/favicon.svg"
        x={EX - LOGO_SIZE / 2}
        y={EY - LOGO_SIZE / 2}
        width={LOGO_SIZE}
        height={LOGO_SIZE}
        opacity={dark ? 0.30 : 0.10}
        style={{ filter: dark ? 'invert(1) sepia(1) saturate(3) hue-rotate(220deg)' : 'none' }}
      />
    </svg>
  )
}
